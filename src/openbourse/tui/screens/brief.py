from openbourse.tui.widgets import HistoryCharts, PriceChart

_CONCERN_GLYPHS = {
    "flagged": ("⚠", "yellow"),
    "clear": ("✓", "green"),
    "unknown": ("?", "dim"),
}


def _risk_color(risk_score: int) -> str:
    """Map a 0-100 risk score to its band colour.

    Mirror of :func:`openbourse.tui.screens.screener._risk_glyph_color` —
    duplicated rather than imported to avoid the circular dependency
    between the brief and screener modules. Bands match README.md:
    0-30 low (green), 30-60 moderate (yellow), 60-100 high (red).
    """
    if risk_score <= 30:
        return "green"
    if risk_score < 60:
        return "yellow"
    return "red"


def _format_brief_body(brief: AiBrief) -> str:
    """Render an :class:`AiBrief` as a Rich-marked-up multi-section block.

    Sections are stacked vertically with colour-coded headers so the user
    can scan bull / bear / risks / concerns at a glance. Concerns render
    as a status-glyph + concern + note grid.
    """
    parts: list[str] = []
    if brief.summary:
        parts.append(f"[b]Summary[/b]\n{brief.summary}")

    if brief.bull:
        parts.append(_format_section("Bull case", brief.bull, color="green"))
    if brief.bear:
        parts.append(_format_section("Bear case", brief.bear, color="red"))
    if brief.risks:
        parts.append(_format_section("Risks to monitor", brief.risks, color="yellow"))

    if brief.concerns:
        max_concern_width = max(len(c.concern) for c in brief.concerns)
        rows: list[str] = []
        for finding in brief.concerns:
            glyph, color = _CONCERN_GLYPHS.get(finding.status, _CONCERN_GLYPHS["unknown"])
            note = f"  [dim]{finding.note}[/dim]" if finding.note else ""
            rows.append(
                f"  [{color}]{glyph}[/{color}] "
                f"{finding.concern.ljust(max_concern_width)}  "
                f"[{color}]{finding.status}[/{color}]{note}"
            )
        parts.append("[b]Concerns checked[/b]\n" + "\n".join(rows))

    parts.append(
        f"[dim]Generated by {brief.model} at {brief.generated_at:%Y-%m-%d %H:%M:%S} UTC[/dim]"
    )
    return "\n\n".join(parts)


def _format_section(title: str, items: tuple[str, ...], *, color: str) -> str:
    """Format one bullet section with a coloured header."""
    bullets = "\n".join(f"  • {item}" for item in items)
    return f"[b {color}]{title}[/b {color}]\n{bullets}"


class BriefScreen(Screen[None]):
    """Per-instrument detail: fundamentals header, history charts, AI brief."""

    BINDINGS: ClassVar[list[BindingType]] = [
        ("escape", "app.pop_screen", "Back"),
        ("b", "app.pop_screen", "Back"),
        ("d", "download_history", "Download history"),
        ("r", "rescan_concerns", "Rescan concerns"),
    ]

    def __init__(
        self,
        *,
        candidate: Candidate,
        providers: Providers,
        history: list[FundamentalsSnapshot] | None = None,
        screen: ScreenDefinition | None = None,
    ) -> None:
        super().__init__()
        self._candidate = candidate
        self._providers = providers
        self._history = history or []
        # The active screen is used to compute the style-fit %; a None
        # screen (e.g., from `bourse lookup`) skips that section.
        self._screen = screen
        # Holds the latest brief so the scanner worker can swap in better
        # concerns and re-render without re-fetching everything else.
        self._brief: AiBrief | None = None
        # Cached filings list so the manual rescan action can re-trigger
        # the worker without going back to the filings provider.
        self._filings: list[Filing] = []

    def compose(self) -> ComposeResult:
        """Render a full-width header, history charts, and the AI brief body."""
        from openbourse.tui.screens.screener import _truncate_summary

        c = self._candidate
        snap = c.snapshot
        price_str = f"${snap.price_usd:,.2f}" if snap.price_usd is not None else "—"
        header = (
            f"[b cyan]{c.instrument.ticker}[/b cyan]  {c.instrument.name}  "
            f"[dim]{c.instrument.sector or ''}  ·  "
            f"{c.instrument.exchange or ''}[/dim]\n"
            f"Price [b]{price_str}[/b]  ·  "
            f"Mkt cap [b]${snap.market_cap_usd / 1e9:.1f}B[/b]  ·  "
            f"Rev growth [b]{snap.revenue_growth_pct:+.1f}%[/b]  ·  "
            f"GM [b]{snap.gross_margin_pct:.1f}%[/b]  ·  "
            f"Net debt/EBITDA [b]{snap.net_debt_to_ebitda:.2f}x[/b]  ·  "
            f"FCF yld [b]{snap.fcf_yield_pct:.1f}%[/b]\n"
            f"Score [b]{c.score}[/b]    "
            f"Risk [b {_risk_color(c.risk_score)}]{c.risk_score}"
            f"[/b {_risk_color(c.risk_score)}]"
        )
        if self._screen is not None:
            fit_pct = compute_style_fit(snap, self._screen)
            header += f"    Style fit [b]{fit_pct:.0f}%[/b]"
        header += f"    Verdict [b]{c.verdict.value}[/b]"
        if c.instrument.business_summary:
            # The brief screen has more vertical room than the detail pane,
            # so we allow a longer truncation here.
            header += (
                "\n[dim italic]"
                + _truncate_summary(c.instrument.business_summary, max_chars=420)
                + "[/dim italic]"
            )
        yield Static(header, id="brief-header")
        # Two-column body: charts on the left, AI brief on the right.
        # Sized 50/50 so the bull/bear/risks/concerns sections fit on
        # screen alongside the price chart and 2x2 fundamentals grid
        # without forcing the user to scroll.
        with Horizontal(id="brief-main"):
            with VerticalScroll(id="brief-left"):
                # Price chart: the right column eats ~half the terminal,
                # so the chart gets ~half the previous width. The widget
                # auto-recomputes on render based on its actual size.
                yield PriceChart(c.instrument.ticker, [], chart_width=70)
                # 2x2 fundamentals trend grid. Self-handles "insufficient
                # history" so ad-hoc lookups still render.
                yield HistoryCharts(self._history)
            with VerticalScroll(id="brief-right"):
                yield LoadingIndicator(id="brief-loading")
                yield Static("", id="brief-body")
        yield Footer()

    async def on_mount(self) -> None:
        """Kick off the brief-loading worker so the UI stays responsive."""
        self.run_worker(self._load_brief(), exclusive=True)

    async def _load_brief(self) -> None:
        """Fetch filings, the AI brief, and 3y price history concurrently.

        Each provider call is independent; running them in ``asyncio.gather``
        means the brief screen renders as soon as the slowest of the three
        completes, not after their cumulative wall time.
        """
        import asyncio

        c = self._candidate

        async def _fetch_filings() -> list[Filing]:
            if not c.instrument.cik:
                return []
            try:
                return await self._providers.filings.recent_filings(c.instrument.cik, limit=3)
            except (OSError, ValueError):  # pragma: no cover - provider edge case
                return []

        async def _fetch_prices() -> list[tuple[date, float]]:
            try:
                return await self._providers.fundamentals.price_history(
                    c.instrument.ticker, period="3y", interval="1d"
                )
            except (OSError, ValueError):  # pragma: no cover - provider edge case
                return []

        filings, price_points = await asyncio.gather(_fetch_filings(), _fetch_prices())
        # The brief generator depends on filings, so it runs after them.
        brief = await self._providers.brief.write_brief(c.instrument, c.snapshot, filings)
        self._brief = brief
        self._filings = filings
        self._render_brief(brief)
        await self._render_price_chart(price_points)
        # Kick off the deep-scan worker as a separate task. Done in the
        # background so the user sees the brief immediately and the
        # concerns section gets refined when the scan completes.
        if filings:
            self.run_worker(
                self._scan_concerns(filings),
                name="concern_scan",
                exclusive=False,
            )

    async def _scan_concerns(self, filings: list[Filing], *, force: bool = False) -> None:
        """Refine the brief's concerns section with evidence from a 10-K scan.

        Steps:
        1. Pick the most recent 10-K from ``filings``.
        2. Check the DB cache by ``(accession, concerns_hash)``.
        3. On miss (or when ``force=True``), fetch the document, extract
           Item 1A, run the scanner, and persist the result.
        4. Merge findings into ``self._brief`` and re-render.

        Failures at any step are silent — the user already has the lighter
        concern findings from the brief generator, and the scan refinement
        is opportunistic.

        ``force=True`` bypasses the cache and is used by the manual rescan
        action. The fresh result still lands in the cache so subsequent
        passive visits benefit from the rescan.
        """
        from openbourse.config import get_settings
        from openbourse.db.engine import create_engine_from_url, get_session_factory

        ten_k = next((f for f in filings if f.form_type.upper() == "10-K"), None)
        if ten_k is None or self._brief is None:
            return

        concerns = list(DEFAULT_CONCERNS)
        engine = create_engine_from_url(get_settings().database_url)
        factory = get_session_factory(engine)
        try:
            findings = await self._scan_with_cache(factory, ten_k, concerns, force=force)
        except Exception:  # pragma: no cover - background polish task
            return
        finally:
            await engine.dispose()

        if not findings:
            return
        self._brief = dataclasses.replace(self._brief, concerns=tuple(findings))
        self._render_brief(self._brief)

    async def _scan_with_cache(
        self,
        factory: async_sessionmaker[AsyncSession],
        filing: Filing,
        concerns: list[str],
        *,
        force: bool = False,
    ) -> list[ConcernFinding]:
        """Cache-aware wrapper around the scanner provider.

        Returns the findings from the cache if present, otherwise fetches
        the filing text, runs the scanner, persists the result, and
        returns it. Empty list signals "scan unavailable" — the caller
        should leave the existing brief findings in place.

        When ``force=True`` the cache lookup is skipped (but a successful
        scan still updates the cache). Use this for the manual rescan
        action where the user wants fresh evidence even if a cache entry
        exists — e.g., after a new 10-K is filed but before our cache key
        catches up.
        """
        from openbourse.db.repositories import ConcernScanRepository

        if not force:
            async with factory() as session:
                repo = ConcernScanRepository(session)
                cached = await repo.get(accession_number=filing.accession_number, concerns=concerns)
                if cached is not None:
                    return cached

        try:
            html = await self._providers.filings.fetch_document(filing)
        except (OSError, ValueError):
            return []
        if not html.strip():
            return []
        section = extract_risk_factors(html)
        if not section:
            return []
        findings = await self._providers.scanner.scan(
            ticker=self._candidate.instrument.ticker,
            filing_text=section,
            concerns=concerns,
        )
        if not findings:
            return []

        async with factory() as session:
            repo = ConcernScanRepository(session)
            await repo.save(
                accession_number=filing.accession_number,
                concerns=concerns,
                findings=findings,
                model=self._brief.model if self._brief else "unknown",
            )
            await session.commit()
        return findings

    async def _render_price_chart(self, points: list[tuple[date, float]]) -> None:
        """Replace the empty placeholder PriceChart with one bound to ``points``."""
        old = self.query_one(PriceChart)
        await old.remove()
        # Mount the populated chart at the top of the left column so it
        # ends up in the same slot the placeholder occupied.
        left = self.query_one("#brief-left", VerticalScroll)
        await left.mount(
            PriceChart(self._candidate.instrument.ticker, points, chart_width=70),
            before=self.query_one(HistoryCharts),
        )

    def _render_brief(self, brief: AiBrief) -> None:
        """Hide the loading indicator and paint the structured brief sections."""
        self.query_one("#brief-loading", LoadingIndicator).display = False
        body = self.query_one("#brief-body", Static)
        body.update(_format_brief_body(brief))

    def action_rescan_concerns(self) -> None:
        """Force a fresh 10-K concern scan, bypassing the DB cache.

        Useful when a new filing has been published but our cache key
        still points at the prior accession, or when the user wants to
        re-run the scan against an updated concerns list. The fresh
        result still lands in the cache so subsequent passive visits
        benefit from the rescan.
        """
        ticker = self._candidate.instrument.ticker
        reason = self._diagnose_rescan_unavailable()
        if reason is not None:
            self.app.notify(
                reason,
                title="Rescan unavailable",
                severity="warning",
                # Longer timeout because the messages are multi-line and
                # tell the user how to fix the underlying setup issue.
                timeout=10,
            )
            return
        self.app.notify(
            f"Rescanning {ticker} 10-K against current concerns…",
            title="Concern rescan",
            timeout=2,
        )
        self.run_worker(
            self._rescan_worker(),
            name="concern_rescan",
            exclusive=False,
        )

    def _diagnose_rescan_unavailable(self) -> str | None:
        """Return an actionable reason if rescan can't proceed, else None.

        Each branch points at a specific failure mode and the concrete
        next step the user should take. The toast that surfaces this
        message is the primary discovery surface for misconfiguration —
        the alternative is the user pressing ``r`` repeatedly and
        guessing why nothing happens.
        """
        ticker = self._candidate.instrument.ticker
        cik = self._candidate.instrument.cik

        if self._brief is None:
            # Brief worker hasn't finished — filings may still be in flight.
            return f"Brief is still loading for {ticker}. Try again in a moment."

        if not cik:
            return (
                f"{ticker} has no CIK on file — EDGAR is keyed by CIK, not ticker.\n"
                "Fix: re-ingest with a provider that includes CIK (FMP) "
                "via `OPENBOURSE_FUNDAMENTALS_PROVIDER=fmp bourse lookup "
                f"{ticker} --history`."
            )

        if self._providers.filings_mode != "live":
            return (
                "EDGAR is in stub mode — no real filings were fetched.\n"
                'Fix: set `OPENBOURSE_EDGAR_USER_AGENT="Your Name '
                'you@example.com"` in your .env and restart `bourse run`. '
                "SEC requires a contact email."
            )

        if not self._filings:
            return (
                f"EDGAR returned no filings for {ticker} (CIK {cik}). "
                "The CIK may be wrong, or the company hasn't filed recently."
            )

        if not any(f.form_type.upper() == "10-K" for f in self._filings):
            forms_seen = ", ".join(sorted({f.form_type for f in self._filings}))
            return (
                f"No 10-K in the most-recent filings for {ticker} "
                f"(saw {forms_seen}). The scanner needs Risk Factors text, "
                "which only 10-Ks reliably contain."
            )

        if self._providers.scanner_mode != "live":
            return (
                "Scanner is in stub mode — set `OPENBOURSE_CLAUDE_API_KEY` "
                "in your .env and restart `bourse run`. The stub scanner "
                "always returns 'unknown' for every concern."
            )

        return None

    async def _rescan_worker(self) -> None:
        """Worker body for :meth:`action_rescan_concerns`.

        Wraps :meth:`_scan_concerns` with a final notify so the user
        sees a confirmation when the fresh evidence has landed in the
        UI (the body update happens silently otherwise).
        """
        await self._scan_concerns(self._filings, force=True)
        self.app.notify(
            f"Concern scan complete for {self._candidate.instrument.ticker}.",
            title="Concern rescan",
            timeout=3,
        )

    def action_download_history(self) -> None:
        """Pull annual history from the live provider, persist, and refresh charts.

        Useful when the brief landed with sparse charts because the ticker
        was looked up ad-hoc (or only its current snapshot was ingested).
        After download the chart panel is rebuilt in place — no need to
        leave and re-enter the brief.
        """
        self.run_worker(self._download_history_worker(), exclusive=False)

    async def _download_history_worker(self) -> None:
        """Worker body for :meth:`action_download_history`."""
        from openbourse.config import get_settings
        from openbourse.db.engine import create_engine_from_url, get_session_factory
        from openbourse.screening import TickerLookupError, lookup_with_history

        ticker = self._candidate.instrument.ticker
        self.app.notify(
            f"Fetching history for {ticker}…",
            title="History download",
            timeout=2,
        )
        engine = create_engine_from_url(get_settings().database_url)
        factory = get_session_factory(engine)
        try:
            async with factory() as session:
                _candidate, history = await lookup_with_history(
                    ticker, self._providers, session=session
                )
        except TickerLookupError as exc:
            self.app.notify(
                str(exc),
                title="Download failed",
                severity="error",
                timeout=6,
            )
            return
        except Exception as exc:  # surface any provider/db error to the user
            self.app.notify(
                f"{type(exc).__name__}: {exc}",
                title="Download failed",
                severity="error",
                timeout=6,
            )
            return
        finally:
            await engine.dispose()

        if not history:
            self.app.notify(
                f"{ticker} has no history available from the configured provider.",
                title="No history",
                severity="warning",
                timeout=4,
            )
            return

        self._history = list(history)
        # Swap the chart widget in place inside the left column so the
        # user sees populated charts without having to back out.
        old = self.query_one(HistoryCharts)
        await old.remove()
        left = self.query_one("#brief-left", VerticalScroll)
        await left.mount(HistoryCharts(self._history))
        self.app.notify(
            f"Loaded {len(history)} history points for {ticker}.",
            title="Download complete",
            timeout=3,
        )
